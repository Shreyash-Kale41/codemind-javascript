console.log(`-------- Do-while --------`);

console.log(`======print numbers from 5 to 15======`);
var i=5;
do{
    console.log(i);
    i++;
}while(i<=15);


console.log(`======print numbers from 50 to 40======`);
var i=50;
do {
    console.log(i);
    i--;
}while (i>=40);

console.log(`======first 15 odd numbers======`);
var i=1;
